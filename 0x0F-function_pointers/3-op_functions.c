#include "3-calc.h"

/**
 * op_add - adds two int
 * @a: the first int
 * @b: the second int
 * Return: the sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two integers
 * @a: the first int
 * @b: the second int
 * Return: the subtracts
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two integers
 * @a: the first int
 * @b: the second int
 * Return: the multiplies
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divids two int
 * @a: the first int
 * @b: the second int
 * Return: tue divides
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo two int
 * @a: the first int
 * @b: the second int
 * Return: the reminder
 */

int op_mod(int a, int b)
{
	return (a % b);
}
